#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	unsigned int	i;
	char			c;

	if (s)
	{
		i = 0;
		c = '\n';
		while (s[i] != '\0')
		{
			write(fd, (s + i), 1);
			i++;
		}
		write(fd, &c, 1);
	}
}
