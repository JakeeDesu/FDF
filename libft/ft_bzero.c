#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*mstr;

	i = 0;
	mstr = (char*)s;
	while (i < n)
	{
		mstr[i] = 0;
		i++;
	}
	s = (void*)mstr;
}
